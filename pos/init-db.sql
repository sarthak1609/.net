DROP DATABASE IF EXISTS cafe;
CREATE DATABASE cafe;
USE cafe;
CREATE TABLE user (id INT AUTO_INCREMENT PRIMARY KEY, username VARCHAR(255) UNIQUE NOT NULL, email VARCHAR(255) UNIQUE NOT NULL, pwd VARCHAR(255) NOT NULL);
CREATE TABLE items (id INT AUTO_INCREMENT PRIMARY KEY, name VARCHAR(255), price DECIMAL(10,2));
CREATE TABLE category (id INT AUTO_INCREMENT PRIMARY KEY, c_name VARCHAR(255));
CREATE TABLE tables (id INT AUTO_INCREMENT PRIMARY KEY, floor VARCHAR(100), capacity INT);
CREATE TABLE sessions (id INT AUTO_INCREMENT PRIMARY KEY, userid INT);
CREATE TABLE orders (id INT AUTO_INCREMENT PRIMARY KEY, status VARCHAR(50), total_amount DECIMAL(10,2));
